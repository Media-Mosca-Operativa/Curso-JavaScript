//Operador de igualdad "=="
var a = 10;
var b = '10';
const resultado_igualdad = a == b;
console.log(resultado_igualdad);  // Salida true 

//Operador de igualdad estricta "==="
var a = 10;
var b = '10';
const resultado_estricta = a === b;
console.log(resultado_estricta);  // Salida false 

//Operador de desigualdad "!="
var a = 10;
var b = '10';
const resultado_desigualdad = a != b;
console.log(resultado_desigualdad);  // Salida false 

//Operador de desisgualdad estricta "!=="
var a = 10;
var b = '10';
const desigualdad_estricta = a !== b;
console.log(desigualdad_estricta);  // Salida true 

//Operador mayor que ">"
var a = 10;
var b = 10;
const resultado_mayor = a > b;
console.log(resultado_mayor);  // Salida false

//Operador menor que "<"
var a = 10;
var b = 10;
const resultado_menor = a < b;
console.log(resultado_menor);  // Salida false 

//Operador mayor o igual que ">="
var a = 10;
var b = 10;
const resultado_mayor_igual = a >= b;
console.log(resultado_mayor_igual);  // Salida true 

//Operador menor o igual "<="
var a = 10;
var b = 10;
const resultado_menor_igual = a <= b;
console.log(resultado_menor_igual);  // Salida true 